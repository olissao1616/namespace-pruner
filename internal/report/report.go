package report

import (
	"bytes"
	"html/template"
	"time"

	"github.com/ag/pruner/internal/cleanup"
	"github.com/ag/pruner/internal/compliance"
)

type NamespaceReport struct {
	Namespace   string
	ClusterURL  string
	OwnerEmail  string
	ScannedAt   time.Time
	TotalImages int
	Violations  []compliance.Violation
	JIRAKeys    []string
	Cleanup     cleanup.Result
}

func (r *NamespaceReport) CountBySeverity(severity string) int {
	n := 0
	for _, v := range r.Violations {
		if v.Severity == severity {
			n++
		}
	}
	return n
}

