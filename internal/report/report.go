func RenderHTML(r *NamespaceReport) (string, error) {
	var buf bytes.Buffer
	if err := htmlTemplate.Execute(&buf, r); err != nil {
		return "", err
	}
	return buf.String(), nil
}

func RenderText(r *NamespaceReport) string {
	var buf bytes.Buffer
	buf.WriteString("=== Image Hygiene Report ===\n")
	buf.WriteString("Namespace : " + r.Namespace + "\n")
	buf.WriteString("Cluster   : " + r.ClusterURL + "\n")
	buf.WriteString("Scanned   : " + r.ScannedAt.Format("2006-01-02 15:04 UTC") + "\n\n")
	buf.WriteString("Total Images : " + itoa(r.TotalImages) + "\n")
	buf.WriteString("Violations   : " + itoa(len(r.Violations)) + "\n")
	buf.WriteString("  HIGH   : " + itoa(r.CountBySeverity("HIGH")) + "\n")
	buf.WriteString("  MEDIUM : " + itoa(r.CountBySeverity("MEDIUM")) + "\n")
	buf.WriteString("  LOW    : " + itoa(r.CountBySeverity("LOW")) + "\n\n")

	if len(r.Violations) > 0 {
		buf.WriteString("VIOLATIONS\n")
		buf.WriteString("----------\n")
		for _, v := range r.Violations {
			buf.WriteString("  [" + v.Severity + "] " + v.ImageRef + " | " + v.RuleType + " | " + itoa(v.AgeDays) + "d\n")
		}
		buf.WriteString("\n")
	}

	if len(r.JIRAKeys) > 0 {
		buf.WriteString("JIRA Tickets: ")
		for i, k := range r.JIRAKeys {
			if i > 0 {
				buf.WriteString(", ")
			}
			buf.WriteString(k)
		}
		buf.WriteString("\n")
	}

	buf.WriteString("\nDeleted: " + itoa(len(r.Cleanup.Deleted)) +
		" | Skipped: " + itoa(len(r.Cleanup.Skipped)) +
		" | Errors: " + itoa(len(r.Cleanup.Errors)) + "\n")
	return buf.String()
}

func itoa(n int) string {
	if n == 0 {
		return "0"
	}
	result := ""
	for n > 0 {
		result = string(rune('0'+n%10)) + result
		n /= 10
	}
	return result
}
